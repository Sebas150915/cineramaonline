                    <script>
                        function openTrailer(videoId) {
                            if (!videoId) return;
                            const modal = document.getElementById('trailerModal');
                            const player = document.getElementById('trailerPlayer');

                            player.src = `https://www.youtube.com/embed/${videoId}?autoplay=1`;
                            modal.classList.add('active');
                            document.body.style.overflow = 'hidden'; // Prevent scroll
                        }

                        function closeTrailer() {
                            const modal = document.getElementById('trailerModal');
                            const player = document.getElementById('trailerPlayer');

                            player.src = ''; // Stop video
                            modal.classList.remove('active');
                            document.body.style.overflow = ''; // Restore scroll
                        }

                        // Global listener for trailer triggers (Use capture phase to bypass stopPropagation from parents)
                        document.addEventListener('click', function(e) {
                            const trigger = e.target.closest('[data-trailer]');
                            if (trigger) {
                                e.preventDefault();
                                e.stopPropagation(); // Stop it early so card clicks don't fire
                                const videoId = trigger.getAttribute('data-trailer');
                                openTrailer(videoId);
                            }
                        }, true);

                        // Close on ESC key
                        document.addEventListener('keydown', function(e) {
                            if (e.key === 'Escape') closeTrailer();
                        });
                    </script>

